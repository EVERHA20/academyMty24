package accesorios;

public class Tv {
	
	public void on() {
		System.out.println("Encender Tv");
	}
	
	public void off() {
		System.out.println("Apagar Tv");
	}
	
	public void activatePause() {
		System.out.println("Pausar Tv");
	}
	
	public void desactivatePause() {
		System.out.println("Reaunuar Tv");
	}

}
